            <div class="indent">
                <h3 data-src="ref0013">(क)  अच्युतकृष्णानन्दकृता - भाष्यटीका </h3>
                <p>अमुद्रितेयं काठकशाङ्करभाष्यटीका महीशूरहस्तलिखितपुस्तकालये <span id="ref0013">(1278 ग्र 22 प My GML)</span> लभ्यते ।  </p>
                <p>अस्याः कर्ता रामानन्दप्रशिष्यस्स्वयम्प्रकाशशिष्य अद्वैतानन्दसरस्वत्याश्च शिष्यः तैत्तरीयव्याख्यावनमालाकारः सप्तदशाष्टादशशतकीय <span class="year">(1650 -  1750 A.D,)</span> अच्युतकृष्णानन्द इति ज्ञायते ।</p>

                <h3 data-src="">(ख)  आनन्दगिरिकृता - भाष्यटीका । </h3>
                <p>मुद्रितश्चायं ग्रन्थ आनन्दाश्रममुद्रणालये । </p>

                <h3 data-src="">(ग)  गोपालबालयतिकृतम् - कठवल्लीभाष्यविवरणम् ।</h3>
                <p>मुद्रिता चेयं व्याख्या आनन्दाश्रममुद्रणालये ।।</p>
                <p>अस्याः कर्ता बालगोपालयतिरित्यपरनामा जगन्नाथाश्रमिशिष्यः, नृसिम्हाश्रमिसतीर्थ्यः, स्वयम्प्रकाशगुरुः, मनीषापञ्चकव्याख्यामधुमञ्जरीकारः षोडशशतकीयः <span class="year">(1500 -  1600 A.D.)</span> गोपालबालयतिरिति ज्ञायते ।।</p>
                
                <h3 data-src="ref0014">(घ)  शिवानन्दयतिकृता - कठभाष्यटिप्पणी </h3>
                <p>अमुद्रितोऽयं ग्रन्थ मद्रासराजकीयहस्तलिखितपुस्तकालये <span id="ref0014">(R 3882 MGOML)</span> लभ्यते । अस्य कर्ता रामनाथविदुष आचार्यः सप्तदश - अष्टादशशतकमध्यवर्ती <span class="year">(1650 -  1751 A.D.)</span> शिवानन्द इति पूर्वमुपपादितम् ।</p>
                
                <h3 data-src="">(ङ)  श्रीधरशास्रिपाठककृता - भाष्यव्याख्या - बालबोधिनी </h3>
                <p>मुद्रिताचेयं व्याख्या पूनानगरे । अस्याः कर्ता त्र्यम्बकशास्त्रिपुत्रः डेक्कान संस्कृतकलाशालाप्रधानाध्यापकः विंशतिशतकीयः <span class="year">(1850 -  1920 A.D.)</span> श्रीधरशास्त्रीति ज्ञायते ।।</p>
