            <p>नचिकेतोमृत्युंसवादरूपायामस्यामुपनिषदि प्रत्येकं वल्लित्रितययुते अध्यायद्वितये सर्वमेधाख्ये यज्ञे दत्तसर्वस्वदक्षिणस्य वास्रवसनाम्नो महर्षेः पुत्रेण नचिकेतोनाम्ना निजनिर्बन्धादेव पित्रा मृत्यवे प्रदत्तेन मृत्युलोकमधिगम्य त्रिस्रो रात्रीरुपोषितवता मृत्युप्रसादादेव निजपितुश्शान्तचित्तत्वं, नचिकेता इति स्वनामधेयोपेतस्याग्नेः ब्रह्मविद्याङ्गतया विज्ञानं सविस्तरं मृत्युनैवोपदिष्टं प्रत्यगात्मपरमात्मनोस्स्वरूपज्ञानं च इत्येतद्वरत्रितयं पुत्रपौत्रचिरायुस्सम्पत्यादिकं प्रत्याचक्षाणेन समधिगतमिति प्रतिपाद्यते । अस्य उपनिषदो रचनाकालः <span class="year">(500 -  400 BC)</span> इति विमर्शकः । मुद्रिता चेयमुपनिषदानन्दाश्रममुद्रणालये । अस्या व्याख्याः - </p>

