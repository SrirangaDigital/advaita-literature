            <p>अमुद्रितोऽयं ग्रन्थः शाङ्करभाष्यानुसारिणी व्याख्यां द्वयोर्मतयोरन्यतमाञ्च स्वीकरोति। ग्रन्थोऽयं बरोडासूच्यां <span id="ref0015">(10058 BRD)</span> दृश्यते । अस्य कर्ता नाराराववेङ्काम्बयोः पुत्रः जमदग्निगोत्रजः क्षत्रियः नारायणगजपतिराय इति परं ज्ञायते ।।</p>
            
