            <h2 data-src="">4. उपनिषद्ब्रह्मेन्द्रकृतम् - छान्दोग्यऋजुविवरणम् । अडयारपुस्तकालये मुद्रितोऽयं ग्रन्थः । </h2>

            <h2 data-src="">5.  त्यागराजशास्त्रिकृतः- सद्विद्याविलासः सव्याख्यः </h2>
            <p>(राजुशास्त्रिकृत ) (रसानुभूतिरिति नामान्तरं व्याख्यायाः) </p>
            <p>छान्दोग्यषष्ठाध्यायसंग्रहात्मकः पद्यात्मकश्चायं ग्रन्थः भाष्यतद्व्याख्यानानुसारं विस्पष्टार्थप्रकाशकः चिदम्बरनगरे ग्रन्थाक्षरेषु मुद्रितः । </p>
            <p>अस्य कर्ता आधुनिकोऽयं प्रकाण्डविद्वान् न्यायेन्दुशेस्वरकर्ता मन्नार्गुडि राजुशास्रीति विख्यातः, त्यागराजपौत्रः. अप्पादीक्षितपुत्रः, भारद्वाजगोत्रजः, यज्ञस्वामिदीक्षितपिता, नारायणसरस्वती - रघुनाथशास्रि - गोपालशास्रिशिष्यः एकोनविंशतिशतकीयः <span class="year">(1815 -  1904 A.D.)</span> त्यागराजदीक्षितः । अदसीया अन्ये ग्रन्था अन्यत्र प्रतिपादिताः ।। </p>

            <h2 data-src="">6. नित्यानन्दाश्रमिकृता -  मिताक्षरा </h2>
            <p>मुद्रिता चेयं मिताक्षरानाम्नी छान्दोग्यव्याख्या बम्बर्इसंस्कृतमुद्रणालये । क्वचिदादर्शपुस्तकेषु बरो़डा - अडयारपुस्तकालयस्थेषु ग्रन्थस्यास्य "अर्थप्रकाशिका" इत्यपि नामान्तरं दृश्यते । </p>
            <p>अस्याः कर्ता पुरुषोत्तमाश्रमशिष्यः, नित्यानन्दाश्रमीति ज्ञायते । एनमधिकृत्य नाधिकं ज्ञातुं पार्यते । मुद्रितपुस्तकात् कालनिर्णययोग्यानि प्रमाणानि नेपलभ्यन्ते । परं तु उज्जयिनीपुस्तकालयस्थे <span class="year">1373</span>  संख्याके आदर्शपुस्तके <span class="year">1746</span>  संवत् इति दृश्यते । यदि स लेखककालस्स्यात् तर्हि सप्तदशशतकात् <span id="year">(1690 AD)</span>  पूर्वतन इति सिध्यति । यदि वा स एव ग्रन्थकर्तुः कालस्स्यात्तर्हि सुवर्णे सौगन्ध्यायितम् । </p>

