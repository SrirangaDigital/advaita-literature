            <p>सामशाखीयायामस्यां छान्दोग्योपनिषदि प्रथमप्रपाठके त्रयोपदशभिः खण्डैः उद्गीथस्यानेकधा उपासनानि. स्तोभाक्षरोपासनम् , तत्फलम् , सामादिज्ञानं तत्फलञ्च प्रतिपाद्यते । द्वितीयप्रपाठके चतुर्विंशतिखण्डैः - साम्नः पञ्चधा सप्तधा चोपासनम् , तत्फलञ्च कथ्यते । तृतीयप्रपाठके एकोनविंशतिखण्डैः आदित्योपासनम् , मधुविद्या, गायत्रीविद्या, शाण्डिल्यविद्या, तासां फलानि चाभिधीयन्ते । चतुर्थप्रपाठके सप्तदशखण्डैः विद्यादानग्रहणविचारः उपकोसलविचारः , ब्रह्मविदो गतिविचारश्च कृतः । पञ्चमप्रपाठके त्रयोविंशतिख्णडैः - प्राणस्य मुख्यत्वविचारः, पञ्चाग्निविद्या, सगुणब्रह्मविद्याफलं आख्यायिकामुखेन वैराग्योदयाय संसारगतिः, वैश्वानरविद्या चाभिधीयते । षष्ठप्रपाठके षो़डशभिः खण्डैराख्यायिकामुखेन ब्रह्मविद्या विचारिता । अनेकधा आत्मतत्वविचार उपदेशश्च कृतः। सप्तमप्रपाठके पञ्चदशभिः खण्डैः भूमविद्या प्रपञ्च्यते । अष्टमप्रपाठके पञ्चदशखण्डैर्दहरविद्या तत्फलं, प्रत्यगात्मविद्या तत्फलं च प्रकाश्यते । अस्या रचनाकालः <span id="year">(700 BC)</span> इति विमर्शकाः । सव्याख्येयमुपनिषन्मुद्रिता आनन्दाश्रममुद्रणालये । अस्या व्याख्या :- </p>

