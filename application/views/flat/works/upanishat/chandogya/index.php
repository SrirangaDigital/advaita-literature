            <h2 data-src="">2. ब्रह्मनन्दिकृतम् - छान्दोग्यवाक्यम् </h2>
            <p>इमौ द्वावपि ग्रन्थौ नोपलभ्येते, तथापि तत्र विभिन्नग्रन्थेषु उद्धरणात् तयोर्ग्रन्थयोस्सत्ता ऊह्यते । एतावधिकृत्य विस्तरेण प्रतिपादितं अद्वैताचार्यप्रकरणे । </p>

