            <h2 data-src="ref0065">8.  विज्ञानात्मकृता - तैत्तरीयकविवृतिः </h2>
            <p>अमुद्रितोऽयं ग्रन्थः मद्रासराजकीयपुस्तकालये <span id="ref0065">(R. 3208 M.G.O.M.L)</span> लभ्यते । अस्य कर्ता परमानन्दमस्करीत्यपरनामा ज्ञानोत्तमस्य शिष्यः चित्सुखाचार्यसतीर्थ्यः द्वादशशतकवासी <span class="year">(1100 -  1200 A.D.)</span> विज्ञानात्मा इति ज्ञायते ।।</p>
            
