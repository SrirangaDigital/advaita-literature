            <h2 data-src="">2. अद्वैतानन्दतीर्थकृता - तैत्तरीयदीपिका </h2>
            <p>"तैत्तरीयोपनिषत्तात्पर्यदीपिका" नाम्ना प्रसिद्धोऽयं ग्रन्थः ब्रह्मविद्यामुद्रणालये मुद्रितः । अस्य कर्ता दक्षिणदेशवासी सदानन्दतीर्थशिष्यः, पूर्वश्रमे माध वसूरि-महालक्ष्म्योः पुत्रः हारीतगोत्रजः, आधुनिकोऽयं अद्वैतानन्द इति ज्ञायते । अदसीया ब्रह्मसूत्रवृत्तिरपि प्रतिपादिता ।। </p>
            
            <h2 data-src="ref0062">3. उपनिषद्ब्रह्मेन्द्रकृतम् - तैत्तरीयविवरणम् ।  <span id="ref0062">(A. L. S.)</span></h2>
            
            <h2 data-src="">4. तारकब्रह्माश्रमिकृताः - तैत्तरीयोपनिषत्सारसंग्रहः</h2>
            <p>अमुद्रितोऽयं ग्रन्थ अडयार पुस्तकालये लभ्यते । अस्य रचयिता दक्षिणदेशीयः रामचन्द्राश्रमिशिष्यः, कल्पतरुपरिमलसंग्रहकर्ता अष्टादशशतकीयः <span class="year">(1700 -  1800 A.D.)</span> तारकब्रह्माश्रमीति ज्ञायते ।। </p>
            
            <h2 data-src="ref0063">5. नारायणाश्रमिकृता - दीपिका । <span id="ref0063">(ASS. 12)</span>  </h2>
            
            <h2 data-src="ref0064">6. बालकृष्णानन्दसरस्वतीकृतम् - तैत्तरीयविवरणम् </h2>
            <p>अमुद्रितोऽयं ग्रन्थः मद्रासराजकीयहस्तलिखितपुस्तकालये <span id="ref0064">(R 383 M.G.O.M.L.)</span> लभ्यते । अस्य कर्ता पद्यमयशारीरकमीमांसाभाष्यवार्तिककर्ता काञ्चीमण्डलान्तर्गतवेदपुरीवासी अभिनवद्रविडाचार्यबिरुदभूषितः, श्रीधरानन्दसरस्वत्याः प्राप्तदीक्षः, गौडब्रह्मानन्दसरस्वत्याश्शिष्यः, महादेवकैलासेशगुरुः सप्तदशशतकीयः <span class="year">(1600 -  1700 A.D.)</span> बालकृष्णानन्दसरस्वतीति ज्ञायते ।</p>
            
            <h2 data-src="">7. भास्करानन्दकृता - तैत्तरीयकव्याख्या</h2> 
