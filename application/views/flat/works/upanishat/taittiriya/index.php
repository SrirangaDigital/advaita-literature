            <p>तैत्तरीयोपनिषद्यस्यां संहित्याख्यायां शिक्षावल्यां अङ्गभूतोपासनोपदेशपूर्वकं परविद्या प्रस्तूयते । आनन्दवल्यां परमतत्वहितपुरुषार्थप्रतिपादनं दृश्यते । वारुण्याख्यायां भृगुवल्यां तपोवधूतकल्मषमनसो ब्रह्मप्रतिपत्तिरित्यभिधीयते । अस्याः रचनाकाल <span class="year">(600 -  500 BC)</span> इति विमर्शकाः । आनन्दाश्रममुद्रणालये मुद्रिताच । अस्या व्याख्या :- </p>
            
            <h2 data-src="ref0053">1. शङ्कराचार्यकृतम् - तैत्तरीयभाष्यम् <span id="ref0053">(ASS 12)</span></h2>
            
            <div class="indent">
                <h3 data-src="">(क) अच्युतकृष्णानन्दकृता - भाष्यव्याख्या वनमाला </h3>
                <p>वनमालाख्या तैत्तरीयशाङ्करभाष्यव्याख्याऽनन्दाश्रमे वाणीविलासमुद्रणालये च मुद्रिता । अस्याः कर्ता अद्वैतानन्दस्वयम्प्रकाशसरस्वतीशिष्यः रामानन्दप्रशिष्यः सिद्धान्तलेशसंग्रहव्याख्याकृष्णालङ्कारकर्ता सप्तदशशतकापरार्धवासी <span class="year">(1650 -  1750 AD)</span> अच्युतकृष्णानन्दतीर्थः ।</p>
                
                <h3 data-src="">(ख) अभिनवनारायणेन्द्रकृता - भाष्यटिप्पणी</h3>
                <p>अमुद्रितोऽयं ग्रन्थः महीशरपुस्तकालये लभ्यते ।</p>
                
                <h3 data-src="ref0054">(ग) आनन्दगिरिकृता - भाष्यटिप्पणी <span id="ref0054">(ASS 12)</span> </h3>
                <p>ज्ञानामृतयतिकृतं व्याख्यानं आनन्दगिरिव्याख्यायाः अस्तीति ज्ञायते ।</p>
                
                <h3 data-src="ref0055;ref0056;ref0057;ref0058;ref0059">(घ) सुरेश्वराचार्यकृतम् - तैत्तरीयभाष्यवार्तिकम् </h3>
                <p>शाङ्करतैत्तरीयकभाष्यस्य पद्यमयी वार्तिकाख्या व्याख्या आनन्दाश्रममुद्रणालये <span id="ref0055">(ASS 13)</span> वाराणस्याञ्च मुद्रिता । अस्याःकर्ता शङ्कराचार्यशिष्येषु अन्यतमः पद्मपादादिसतीर्थ्यः अष्टमशतकवासी <span id="year">(800 AD)</span> सुरेश्वराचार्यः । </p>
                <p>पूर्वाश्रमे शोणानदीतिरवासी पञ्चगौडान्तर्गतः कुमरिलभट्टजामाता पूर्वकाण्डप्रवर्तकः मण्डनमिश्र इति ख्यातः विश्वरूप एव सन्यासस्वीकारादनन्तरं सुरेश्वर इति प्रसिद्ध इति साम्प्रदायिकाः वदन्ति । </p>
                <p>"जागोप् महाशयेन नैष्कर्म्यसिद्धिभूमिकायां मण्डनमिश्रसुरेश्वरविश्वरूपाणामैक्यं स्वीक्रियते । सप्तदशशतकीयेन बालकृष्णानन्दसरस्वत्या कृते शारीरकमीमांसाभाष्यवार्तिके मण्डनमिश्रसुरेश्वरविश्वरूपाणामैक्यमेवोपवर्णितम् । विद्यारण्यै र्विववरणप्रमेयसंग्रहे बृहदारण्यकभाष्यवार्तिकादुद्धरणं दत्तम् । तत्रापि विश्वरूपशब्देन सुरेश्वर एव निर्दिष्टः ।।"</p>
                <p>दासगुप्तस्तु सुरेश्वरविश्वरूपावभिन्नौ मण्डनमिश्रस्तु भिन्न इति <span id="ref0056">(HIP Vol .II)</span> वदति । हिरियण्णामहाशयस्तु <span id="ref0057">(J. R. A. S. 1924)</span>  जर्नल आफ रायल आसियारिक सोसाइटि पत्रिकायां सुरेश्वरः मण्डनादन्य इति प्रतिपादयति । </p>
                <p>कुप्पुस्वामिशास्रिणस्तु ब्रह्मसिद्धिभूमिकायां सुरेश्वरब्रह्मसिद्धिकारयोस्सिद्धान्तगतभेदमुपवर्ण्य ब्रह्मसिद्धिकारः सुरेश्वरादन्य इति प्रतिपादयन्ति। </p>
                <p>संक्षेपशारीरककर्ता सर्वज्ञात्मा सुरेश्वरशिष्य इति तु प्रसिद्धिः । सुरेश्वरश्च देवेश्वरशब्देन सर्वज्ञात्मना निर्दिष्ट इति तु पण्डितपरम्परागता वार्ता । </p>
                <p>श्रीकण्ठशास्त्री <span id="ref0058">(I.H.Q. val. XIV)</span> एवं <span id="ref0059">(J.O.R. 1937)</span> पत्रिकायां नायं सर्वज्ञात्मगुरुस्सुरेश्वर इति प्रतिवादयति । देवेश्वरस्त्वन्य इति च प्रतिपादयति । चिन्तामणिमहोदयेन च अङ्गीक्रियते । निरूपितञ्चैतदस्माभिर्विस्तरेण सर्वज्ञात्मप्रसङ्गे । </p>
                <p>श्रीकण्ठशास्त्रिणा प्रदर्शितायां शृङ्गगिरिगुरुपरम्परायां नित्यबोधघनाभिधः नित्यबोधाचार्य एव सुरेश्वरशिष्य इति निर्दिश्यते । नित्यबोधाचार्यकालश्च <span class="year">773 - 848</span>  इति च निर्दिश्यते । तस्मात् सर्वज्ञात्मन एव नित्यबोधाचार्य इति नामान्तरस्वीकारोऽपि सुष्ठु लगति । </p>
                <p>कुप्पुस्वामिशास्त्रिसिद्धान्तानुसारं, श्रीकण्ठशास्त्रिप्रदर्शितशृङ्गगिरिपरम्परानुसारञ्च सुरेश्वरकालः सप्तमाष्टमशतकमिति <span class="year">(620 -  777 A.D)</span> इति सिध्यति । </p>
            
