                <h1>अथर्वशिखोपनिषत्</h1>
                <p>अस्यामुपनिषदि सकलवेदमूलभूतस्य प्रणवस्य स्वरूपं, तदीयमात्राणां देवतादयः, प्रणववाचकानां ओङ्कारतारकादिपदानां व्युत्पत्तिः, तद्ध्यानध्यातृघध्येयस्वरूपं च इत्येतत् सर्वं निरूप्यते । इयमुपनिषद् आनन्दाश्रममुद्रणालये मुद्रिता । अस्या व्याख्याः - </p>

                <h2>नारायणाश्रमिकृता - अथर्वशिखोपनिषद्दीपिका </h2>
                <p>अथर्वशिखोपनिषदां व्याख्यात्मकोऽयं ग्रन्थः आनन्दाश्रममुद्रणालये मुद्रितः । अस्य कर्ता श्रीनाथपौत्र रत्नाकरभट्टपुत्रः आनन्दात्मशिष्यः नारायणाश्रम इति ज्ञायते । अनेन विरचितायां " माणडूक्योपनिषद्दीपिकायां " अमुद्रितायां सरस्वतीमहालयपुस्तकालयस्थायां (1556 D.C.T.S.M.L) अानन्दगिरिर्निर्दिष्टः । आनन्दाश्रममुद्रितायां जाबालोपनिषद्दीपिकायां आनन्दात्मा अध्यात्मगुरुरिति निर्दिष्टः। आनन्दात्मा तु शङ्करानन्दस्यापि गुरुरिति नारायणाश्रमस्य कालः त्रयोदशचतुर्दशशतकम् (1275 - 1350 A.D) इति निश्चीयते ।। </p>

                <h2>शङ्करानन्दविरचिता - अथर्वशिखोपनिषद्दीपिका </h2>
                <p>आनन्दात्मनः विद्यातीर्थस्य च शिष्योऽयं शङ्करानन्दः विद्यारण्यस्य गुरुः त्रयोदशशतकारम्भकालवासी (1275 - 1350 A.D.) इति ज्ञायते । अमुद्रितोऽयं ग्रन्थः सरस्वतीमहालयसूच्यां (1427 TSML) अडयारपुस्तकालये बरोडापुस्तकालये च दृश्यते । शङ्करानन्दमधिकृत्याधिकं सूत्रवृत्तिप्रकरणे प्रकरणग्रन्थप्रकरणे अद्वैताचार्यप्रतिपादनावसरे च प्रतिपाद्यते ।। </p>

                <h2>शङ्कराचार्यकृतं - अथर्वशिखोपनिषद्भाष्यम् (१) </h2>
                <p>ग्रन्थोऽयं शङ्कराचार्यकृतत्वेन अडयारपुस्तकालयस्थे (30 B 22 ग्र 6 AL) आदर्शपुस्तके दृश्यते । अमुद्रितश्चायं ग्रन्थः । अस्य कर्ता न प्रसिद्धश्शङ्कराचायों भवितुमर्हति । तथा प्रसिद्धेरभावात् । अन्तरङ्गपरीक्षायान्तु कृतायां सर्वथा न शङ्कराचार्यकृतोऽयं ग्रन्थ इत्येव प्रतीयते । यतः - यासामुपनिषदां शङ्कराचार्यैर्व्याख्या कृता तासां न व्याख्या कृता तासां व्याख्याप्रस्तावे नारायणाश्रमिणा " शङ्करोक्त्युपजीविना " इत्युच्यते । यासां न व्याख्या कृता तासां व्याख्याप्रस्तावे " श्रुतिमात्रोपजीविना " इत्येव निर्दिश्यते । दृश्यते चात्र श्रुतिमात्रोपजीविना इति । तस्मादपि कारणात् नेयं व्याख्या शङ्कराचार्यकृता इत्येव प्रतिभाति ।। </p>
                <p>उपनिषद्ब्रह्मेन्द्रकृतं - विवरणम् अडयार पुस्तकालये मुद्रितम् । </p>
