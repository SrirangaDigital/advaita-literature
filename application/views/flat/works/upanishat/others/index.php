                <p>अस्यामुपनिषदि देवानां को भवानिति रुद्रं प्रति प्रश्नः, तदुत्तरेण तस्य सर्वात्मकत्वज्ञानेन तन्नतिपूर्वकं बहुधा तस्य स्तुतिः, तत्प्रतिपादकत्वेन प्रसिद्धानां " ओङ्कार - प्रणव - सर्वव्याप्यनन्तादिपदानां निर्वचनं, इत्यादयो विषयाः प्रतिपादिताः । ग्रन्थोऽयमानन्दाश्रममुद्रणालये मुद्रितः । अस्या व्याख्याः - </p>
                
                <h2>नारायणाश्रमकृता - अथर्वशिर उपनिषद्दीपिका </h2>
