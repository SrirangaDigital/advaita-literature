                <h1>श्वेताश्वतरोपनिषत्</h1>
                <p>अस्यामुपनिषदि क्षराक्षरस्वरूपकथनपूर्वंकं योगक्रममुपपाद्य योगगम्यस्य परमात्मनस्स्वरूपादिकं तस्य पुरुषसूक्तप्रतिपाद्यात्वमोक्षप्रदत्वादिकं चोपवर्ण्य, प्रकृतिस्वरूपं विविच्य परमात्मनो गायत्रीप्रतिपाद्यत्वमुक्त्वा क्षराक्षरविद्याफलभेदादिकं प्रकाश्य परमात्मस्वरूपतदुपासनातन्महिमादिकं प्रपञ्च्यते । श्वेताश्वतरविद्वदुपदिष्टत्वादियं श्वेताश्वतरोपनिषदिति कथ्यते । अस्या रचनाकालः (200 - 100 BC)  इति विमर्शकाः । मुद्रिता चेयमानन्दाश्रममुद्रणालये । (ASS 17) </p>
  
                <h2>1. शङ्कराचार्यकृतम् - श्वेताश्वतरभाष्यम् । (ASS 17)</h2>
                <p>शङ्कराचार्यकृतत्वेनेदं भाष्यं मुद्रितमानन्दाश्रममुद्रणालये । सरस्वतीमहालयस्थादर्शग्रन्थान्ते (1565 TSML) च शङ्कराचार्यकृतमिति दृश्यते । परन्तु शङ्कराचार्यैः श्वेताश्वतराणां भाष्यं न कृतमिति ज्ञायते । अथवा नेदं भाष्यं शङ्कराचार्यकृतमिति निश्चीयते । अत्रेमानि कारणानि । </p>
                <p>(1) विद्यारण्यप्रणीतशङ्करविजये षष्ठसर्गे आचार्यशङ्करकृतोपनिषद्भाष्यादिनामनिर्देशे श्वेताश्वतरोपनिषद्भाष्यनामानिर्देशः । तत्रहि - </p>
                <p>"करतलकलिताद्वयात्मतत्वं क्षपितदुरन्तचिरन्तनप्रमोहम् ।</p>
                <p>उपचितमुदितोदितैर्गुणौघैः उपनिषदामयमुज्जहार भाष्यम् ।।"</p>
                <p>अत्र धनपतिसूरिकृतडिण्डिमाख्यटीकायां उपनिषदामित्येतत्पदव्याख्याने " ईशादिबृहदारण्यकान्तानां दशानामेवोपनिषदां संग्रहः कृतः। यदि श्वेताश्वतरोपनिषदामपि भाष्यं शङ्कराचार्यैः प्रणीतं स्यात्तर्हि डिण्डिमकारेणास्या उपनिषदोऽप्युल्लेखः कृतो भवेत् । "</p>
                <p>(2) यासामुपनिषदां भाष्यं शङ्कराचार्येण कृतं तासाम् , यासामुपनिषदां शङ्कराचार्येण भाष्यं न कृतं तासामपि दीपिकानाम्नी व्याख्या नारायणाश्रमिणा रचिता आनन्दाश्रममुद्रणालये मुद्रिता च । तासु शङ्कराचार्यकृतत्वेन निश्चितभाष्याणानुपनिषदां व्याख्यावसरे नारायणाश्रमिणा " शङ्करोक्त्युपजीविना " इत्युच्यते । तासुदीपिकासु दृश्यते वाक्यतोऽपि साम्यं भाष्येण । शङ्कराव्याख्यातानामुपनिषदां व्याख्यावसरे "श्रुतिमात्रोपजीविना " इत्येव दीपीकायां निर्दिश्यते । श्वेताश्वतरदीपिकापि श्रुतिमात्रोपजीविना नारायणाश्रमिणैव कृता न तु शङ्करोक्त्युपजीविना । तस्मात् श्वेताश्वतरोपनिषदां शाङ्करं भाष्यं नास्तीत्येव ज्ञायते । </p>
                <p>(3) नारायणविरचिते दीपिकाख्ये व्याख्याने षष्ठेऽध्याये - " यदा चर्मवदाकाशं " इत्यादिविंशतितमर्क्व्याख्यानावसरे " अयमर्थ आचार्यसम्मतः । चर्मवदाकाशवेष्टनासम्भववत् अविदुषो मोक्षासम्भववत् अविदुषो मोक्षासम्भवश्रुतेरिति सर्वधरर्मान् परित्यज्येति श्लोके शाङ्करगीताभाष्ये उक्तत्वादिति " विद्यते । यदि श्वेताश्वतरोपनिषदः भाष्यं कृतं स्यात्तर्हि गीताभाष्ये उक्तत्वादिति हेतुरसङ्गतः । एतद्भाष्ये एव आचार्यैरुक्तत्वादित्येव वक्तुं शक्याम् । </p>
                <p>(4) आचार्यशङ्करप्रणीतदशोपनिषद्भाष्येषु दृश्यमाणाः पदलालित्य गाम्भीर्य सरलतादयो गुणाः अस्या भाष्ये न दृश्यते । तस्मात् शङ्करपीठाधिष्ठितैः अन्यैर्वा शङ्कराचार्याणां नाम्ना केनापि विदुषा वा कृतं स्यादित्येव निर्णीयते । शाङ्करभाष्याणां सर्वेषामपि आनन्दगिरिणा व्याख्या कृता श्रूयते । परन्तु श्वेताश्वतरोपनिषद्भाष्यस्य आनन्दगिरिकृता व्याख्या नाद्यापि लक्ष्यते च ।</p>

