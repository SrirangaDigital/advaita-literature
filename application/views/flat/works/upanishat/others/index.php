            </section>
            <section>
                <h1>कौषीतक्युपनिषत्</h1>
                <p>ऋक्शाखीयायामस्यामुपनिषदि फलसङ्गाद्यभिसन्धियुतानां धूमादिमार्गेण चन्द्रमसं प्राप्य पुनरावृत्तानां संसृतौ भ्रमणम् , विद्याकर्मविहीनानां कीटपतङ्गाद्यात्मना क्लेशानुभूतिः, गुर्वनुग्रहाधिगतविद्यानां ब्रह्मचारिगृहिप्रभृतीनां अर्चिरादिमार्गेण उत्तमां गर्ति प्रपन्नानां ब्रह्मणस्सारूप्यर्माधगतानां ब्रह्मालङ्कारपरिमण्डितानां परब्रह्मानुभवप्रकारः, इन्द्रप्रतर्दनाख्यायिकया स्वदेहभोगस्य प्रारब्धपरिक्षयैकपर्यवसायित्वकथनम् ,प्राणोपासनाभिधानम् , अजातशत्र्त्वाख्यायिकया आदित्य - चन्द्र - अग्निविद्युत् - स्तनयित्वादिषु उपासनासु विसंवादप्रदर्शनपूर्वकं परमात्मस्वसूपतद्विज्ञानादिप्रकाशनमित्येतत्सर्वं प्रतिपाद्यते । अस्या रचनाकालः 200 - 100 BC  इति विमर्शकाः। मुद्रिता चेयमुपनिषत् सव्याख्याऽनन्द्राश्रममुद्रणालये ।। अस्या व्याख्या :-</p>

