            <h2>2. उपनिषद्ब्रह्मेन्द्रकृतम् - बृहदारण्यकविवरणम् ।। (ALS)</h2>
            
            <h2>3.  द्रविडाचार्यकृतम् - बृहदारण्यकभाष्यम् । </h2>
            <p>भाष्यमिदं नोपलभ्यते । तथापि शङ्करात् पूर्वतनेन द्रविडाचार्येण कृतमिति भाष्यादिषु उद्धृतत्वात् ज्ञायते । एतच्च द्रविडाचार्यप्रकरणे प्रतिपादयिष्यते । </p>
            
            <h2>4. नारायणाश्रमिकृता - दीपिका ।</h2> 
            <p>यद्यपीयं दीपिका नोपलभ्यते तथापि जाबालोपनिषद्दीपिकायाः 275 तमे पुटे ग्रन्थकृतैव निर्दिष्टा ।। </p>
            
            <h2>5. नित्यानन्दाश्रमकृता - मिताक्षरा । (ASS 31) </h2>
            
            <h2>6. वासुदेवब्रह्मकृता - प्रकाशिका ।</h2> 
            <p>अमुद्रितोऽयं बरोडासूच्यां दृश्यते । अस्य कर्ता अनिरूद्धपुत्रः हृषीकेशशिष्यः वासुदेवब्रह्म एकोनविंशतिशतकीय (1800 - 1909 A.D.) इति ज्ञायते । </p>
            
            <h2>7. विद्यारण्यकृता - बृहदारण्यकव्याख्या ।</h2>  
            <p>ग्रन्थोऽयममुद्रितः नासिकसूच्यां दृश्यते ।</p>
            
