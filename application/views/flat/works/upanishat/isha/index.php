            <p>अस्यामुपनिषदि चिदचित्स्वरूपस्य जगतः परमात्माधीनस्वरूपस्थित्यादिमत्वं, आदेहपातं यथाशक्ति ब्रह्मविद्याङ्गभूतकर्मयोगस्यानुष्ठेयत्वं, अविदुषो निन्दनं, परमात्मनः विचित्रानन्तशक्तिमत्वं, ब्रह्मात्मकजगदनुसन्धानस्य फलं, ईशेशितव्यवेदिनः ज्ञानयोगाद्युपदेशः केवलकर्मयोगावलम्बिनां विनिन्दनं भगवद्भक्तिनिष्ठस्यावश्यानुसन्धेयोपदेश इत्यादिकमुपवर्णितं दृश्यते । यजुर्वेदीयेयमुपनिषत् आनन्दाश्रममुद्रणालये (ASS 5) मुद्रिता । अस्या उपनिषदः रचनाकालः क्रिस्तोः पूर्वं सप्तमशतकम् (700 BC) इति विमर्शकाः। अस्याः व्याख्याः, उपव्याख्याश्च- </p>
            
            <h2>(क) शङ्कराचार्यकृतं - ईशावास्योपनिषद्भाष्यम् </h2>
            <p>मुद्रितञ्चेदं भाष्यमानन्दाश्रममुद्रणालये (ASS 5)। शङ्कराचार्यकालादि सविस्तरं प्रकरणग्रन्थप्रस्तावे अद्वैताचार्यप्रस्तावे च प्रतिपाद्यते । </p>
            
            <div class="indent">
                <h3>(A) आनन्दगिरिकृता - ईशावास्यभाष्यव्याख्या</h3>
                <p>व्याख्यायामस्यां "तत्वालोकः " निर्दिष्टः । भास्करमतं खण्डितञ्च । मुद्रितश्चायं ग्रन्थ आनन्दाश्रममुद्रणालये (ASS 5) ।</p>
                <p>व्याख्याया अस्याः कर्ता आनन्दज्ञानापराभिध आनन्दगिरिः । सन्यासस्वीकारात्पूर्वं जनार्दन इत्यस्यैव नाम । गुजरातप्रान्तजोऽयं द्वारकास्थ शङ्करपीठाधीश आसीदिति प्रसिद्धिः। आन्ध्रदेशज इति साम्प्रदायिकाः । अनुभूतिस्वरूपाचार्यशुद्धानन्दयोशिशष्योऽयं अखण्डानन्दस्य प्रज्ञानानन्दस्य च गुरुः, कलिङ्गदेशाधिपतेर्नृसिम्हदेवस्य सामयिकस्त्रयोदशशतकीय (1260 - 1320 A.D) इति ज्ञायते । अदसीया अन्ये ग्रन्थाः प्रकरणप्रस्तावे अद्वैताचार्यप्रस्तावे च प्रतिपाद्यन्ते ।। </p>
                <h3>(B) शिवानन्दयतिकृतं - ईशावास्यभाष्यटिप्पणम् </h3>
                <p>अमुद्रितोऽयं पूर्णग्रन्थः मद्रासराजकीयहस्तलिखितपुस्तकालये (R 3882 M.G.O.M.L) लभ्यते ।</p>
                <p>अस्य कर्ता रामनाथविदुष आचार्यस्सप्तदशाष्टादशशतकमध्यवर्ती (1650 - 1750 A.D) शिवानन्दयतिरिति ज्ञायते । अनेनरचित आनन्ददीपाख्य. प्रकरणग्रन्थः अन्यत्र निरूपितः ।। </p>
