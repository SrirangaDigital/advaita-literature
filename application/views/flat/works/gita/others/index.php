            <section>
                <h1>ब्रह्मगीता II</h1>
                <p>स्कान्दपुराणान्तर्गतसूतसंहितान्तर्गतेयं गीता गीतासंग्रहे मुद्रिता । शिवभक्ति अद्वैतसिद्धान्तप्रदर्शनप्रधानेयं गीता दशोपनिषदामर्थञ्च प्रतिपादयति । </p>
                <h2>1. कृष्णानन्दकृता - चित्प्रकाशिनी </h2>
                <p>ब्रह्मगीतव्याख्यापरनामायं ग्रन्थः नासिकसूच्यां 136 दृश्यते । अस्य कर्ता कैवल्यानन्दकृष्णानन्दयोश्शिष्यः वाराणसीवासी एकोनविंशतिशतकीयः (1800 - 1900 A.D.) कृष्णानन्दसरस्वतीति ज्ञायते ।।</p>
                <h2>2. माधवाचार्यकृता - तात्पर्यदीपिका </h2>
                <p>ब्रह्मगीताव्याख्यात्मकोऽयं र्ग्रन्थः मद्रासनगरे बालमनोरमामुद्रणालये मुद्रितः । अस्य कर्ता माधवाचार्यः माधवमन्त्रीत्यपरनामा आङ्गिरसगोत्रजः. चावुण्ड्यात् माचाम्बिकायामुत्पन्नः काशीविलासक्रियाशक्तिशिष्यः परमशिवभक्तोऽपि अद्वैत वेदान्ततत्ववेत्ता प्रचण्डयोद्धा शत्रुमानमर्दनकारी शौर्यसम्पन्नः, उपनिषन्मार्गप्रवर्तकाचार्यविरुदभूषितः, चतुर्दशशतकीयः (1340 - 1391 A.D.) इति ज्ञायते । माधवमन्त्री अयं भारद्वाजगोत्रजात् , मायणश्रीमत्योः पुत्रात् , सायणभोगनाथयोः भ्रातुः, विद्यातीर्थभारतीतीर्थश्रीकण्ठशिष्यात् , पराशरमाधवपञ्चदश्यादिकर्तुः विद्यारण्यापराभिधानात् माधवाचार्यात् भिन्न इति यफिग्राफिका कर्नाटिका (Vol. 7 ) शिकारपुर 281,  एवं - यफिग्राफिकाकर्नाटिका (Vol. 8) प्रमाणाच्च, ज्ञायते । </p>
                <h2>3. वेङ्कटेशशास्त्रिकृता - तात्पर्यबोधिनी ।</h2>
                <p>अमुद्रितोऽयं ब्रह्मगीताव्याख्याग्रन्थः मद्रासराजकीयपुस्तकालये (R. 4065 MGOML) लभ्यते । अस्य कर्ता आत्रेयगोत्रजः कोयम्पुरि (कोयम्पुत्तूर ?) वासी सर्वदेवशिष्यः अय्यात्तुरेशास्त्रीत्यपरनामा सामिशास्त्रिसामयिकः एकोनविं शतिशतकीयः (1790 - 1850 A.D.) वेङ्कटेश्वरशास्त्रीति ज्ञायते ।। </p>
            </section>
            <section>
                <h1>रामगीता</h1>
                <p>तैत्तरीयोपनिषदः, वाजसेनेयश्रुतिञ्च प्रमाणयन्तीयं रामगीता तत्वसारायणान्तर्गता आत्मविद्याग्रन्थमालायां (1) आष्टेकर कम्पनिमुद्रितगीतासंग्रहे च मुद्रिता । रामलक्ष्मणसंवादशैल्या प्रवृत्तोऽयं ग्रन्थः ज्ञानादेव मोक्ष अज्ञाननिवृत्तिश्चेति प्रतिपादयति ।</p>
            </section>
            <section>
                <h1>रुद्रगीता</h1>
                <p>बृहद्ब्रह्मसंहितान्तर्गतेयं गीता रुद्रभद्रवाहुसंवादमुखेन प्रवृत्ता अद्वैतवेदान्तविषयिणी आनन्दाश्रममदितायां बृहद्ब्रह्मसंहितायां द्रष्टव्या ।</p>
            </section>
            <section>
                <h1>वासिष्ठगीता</h1>
                <p>निर्णयसागरमुद्रणालयमुद्रितयोगवासिष्ठोत्तरार्घगतनिर्वाणप्रकरणान्तर्गता आत्मविश्रान्तिस्वभावविश्रान्तिविषणीयं वासिष्ठगीतेति ज्ञायते ।। </p>
            </section>
            <section>
                <h1>शिवगीता</h1>
                <p>षोडशभिरध्यायैः पूर्णेयं गीता अगस्त्योपदेशमुखेन प्रवृत्ता आष्टेकरकम्पनीमुद्रणालये मुद्रिता ।। अस्या व्याख्याः -</p>
                <h2>1.  परमशिवेन्द्रसरस्वतीकृता - शिवगीतातात्पर्यप्रकाशिका । </h2>
                <p>मुद्रितोऽयं ग्रन्थः वाणीविलासमुद्रणालये अस्य कर्ता परमशिवेन्द्रः ज्ञानेन्द्रसरस्वतीप्रशिष्यः अभिनवनारायणेन्द्रशिष्यः, सदाशिवब्रह्मेन्द्रगुरुः सप्तदशशतकीय इति (1600 - 1700 A.D.) इति ज्ञायते ।।</p>
                <h2>2. ब्रह्मानन्दसरस्वतीकृता शिवगीताव्याख्या ।</h2>
                <p>अमुद्रितोऽयं ग्रन्थः मद्रासराजकीयपुस्तकालये (R. 926 A. MGOML) लभ्यते । अस्याः कर्ता ब्रह्मानन्दस्समराज सरस्वत्याः सदाशिवतीर्थस्य च शिष्य इति परं ज्ञायते ।।</p>
                <h2>3. लक्ष्मीनरहरिसूनुकृता - बालानन्दिनी । </h2>
                <p>शिवगीताव्याख्यात्मकोऽयममुद्रितग्रन्थः सरस्वतीमहालये (9022 DC.TSML) दृश्यते । अस्य कर्ता भट्टोजीदीक्षितादर्वाचीन इति परं ज्ञायते ।।</p>
                <h2>4. शङ्करानन्दकृता - शिवगीताव्याख्या ।</h2>
                <p>अमुद्रितोऽयं ग्रन्थः अडयारपुस्तकालये (9 B 42 क 3 AL) लभ्यते । अस्य कर्ता शङ्करानन्दः भगवद्गीताव्याख्यावसरे प्रतिपादितः ।। </p>
            </section>
            <section>
                <h1>शिवरामगीता</h1>
