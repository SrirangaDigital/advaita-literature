                <h1>अगस्त्यगीता</h1>
                <p>बिब्लियोथिका - इण्डिकाग्रन्थमालायां कल्कत्तानगरे मुद्रितस्य वराहपुराणस्य एकपञ्चाशत्तमादध्यायादारभ्य त्रिपञ्चाशत्तमाध्यायपर्यन्तेयं गीता (51 - 53) अगस्त्यगीतेति कथ्यते । </p>
                <p>भद्राश्व - अगस्त्यसंवादरूपायां अस्यां गीतायां अहङ्कार - अज्ञान - विज्ञानेन्द्रियाणामुत्पत्तिः, जगत्सृष्टिः, ज्ञानस्वरूपमित्येते विषयाः पशुपालकथाव्याजेन निरूपिताः ।। </p>
            </section>
            <section>
                <h1>अनुगीता</h1>
                <p>महाभारतान्तर्गतेयं गीता। महाभारतस्याश्वमेधिकपर्वणि षोडशाध्यायादारभ्या प्रवृत्तः कृष्णार्जुनसंवादः अनुगीताशब्देन व्यवहृतः । पूर्वोपदिष्टगीतार्थस्य संग्रामव्यग्रस्य अत एव विस्मृतगीतार्थस्यार्जुनस्य प्रार्थनया भगवान् भगवद्गीतार्थं सङूगृह्णाति । ज्ञानस्याविनाशित्वं, ज्ञानादेव मुक्तिरिति च प्रतिपादयन्तीयं गीता मुद्रिता ।। (S.B.E.8) </p>
                <h2>1. गौडपादमुनिकृतम् - अनुगीताभाष्यम् ।</h2>
                <p>अमुद्रितोऽयं ग्रन्थः नासिकहस्तलिखितपुस्तकालयसूच्यां (XXVI 35) दृश्यते । गौडपादमुनिश्च माण्डूक्यप्रकरणे उपपदितः । </p>
            </section>
            <section>
                <h1>अवधूतगीता</h1>
                <p>दत्तगीता, जीवन्मुक्तगीता इत्यादिनाम्ना प्रसिद्धेयं गीता 'आष्टेकर कम्पेनि ' पूनानगरे मुद्रिता । गोरक्षदत्तात्रेयसंवादरूपेऽस्मिन् ग्रन्थे जीवन्मुक्तस्य स्वरूपं, मोक्षस्वरूपं आत्मज्ञानविचारश्च सम्यगुपपादिताः । अस्य कर्ता यदि महाभारतादिषु प्रसिद्धः दत्तात्रेयस्स्यात्तर्हि अत्रेरनसूयायाश्च पुत्र, निमेः पिता, श्रीमतः पितामहः इति महाभारतानुशासनपर्व - सभापर्वप्रमाणात् निर्णेतुं शक्यते । </p>
                <h2>1.  परमानन्दतीर्थकृता - अवधूतगीताटीका ।</h2>
                <p>अमुद्रितोऽयं ग्रन्थ मद्रासराजकीयहस्तलिखितपुस्तकालये (D. 17476 MGOML) तिरुवनन्तपुरपुस्तकालये महीशूरपुस्तकालये च लभ्यते । अस्याः कर्ता परमानन्दतीर्थः विद्यातीर्थप्रशिष्यः भारतीतीर्थशिष्यः चतुर्दशशतकीय (1300 - 1400 A.D.) इति निश्चीयते । </p>
                <h2>2. शङ्कराचार्यकृताः - अवधूतगीतासारः ।</h2> 
                <p>अमुद्रितोऽयं ग्रन्थः (D. 18884 MGOML) लभ्यते । </p>
                <h2>3. अज्ञातकर्तृका - अवधूतगीताव्याख्या ।</h2> 
                <p>अमुद्रितोऽयं ग्रन्थः (R. 5498 MGOML) लभ्यते ।। </p>
            </section>
            <section>
                <h1>अष्टावक्रगीता</h1>
                <p>अष्टावक्रीयं, अष्टावक्रसूक्तं, अवधूतानुभूतिरित्यादिनामभिः प्रसिद्धेयं गीता एकविशतिभिरध्यायैः पूर्णा अष्टावक्रजनकसंवादरूपेण अद्वैतवेदान्तसिद्धान्तान् ब्रह्मणोऽद्वितीयत्वं चिन्मयत्वञ्च प्रतिपादयति । मुद्रिता चेयं गीता आष्टेकर कम्पेनिपूनानगरे ।</p>
                <h2>1. पूर्णानन्दकृता - अष्टावक्रगीताव्याख्या । </h2>
                <p>ग्रन्थोऽयं मध्यप्रान्तीय वरार्ग्रन्थसूच्यां (327 CCPB) दृश्यते । यद्ययं पूर्णानन्दः ब्रह्मविद्याभरणकाराणां अद्वैतानन्देन्द्राणां शिष्यः, रामानन्दस्य प्रशिष्यः, अच्युतकृष्णानन्दसतीर्थ्यः, भाष्यरत्नप्रभाव्याख्यापूर्णानन्दीयकर्ता स्यात् तर्हि अस्य कालः (1650 - 1750 A.D) सप्तदशशतकीय इति निर्णेतुं शक्यते ।। </p>
                <h2>2. भासुरानन्दकृता - अष्टावक्रगीताव्याख्या ।</h2>
                <p>ग्रन्थोऽयं मध्यप्रान्तीयबरार्ग्रन्थसूच्यां दृश्यते । भासुरानन्दोऽयं काशीवासीसन्नपि चोलदेशानागतः कावेरीतीरवासी सञ्जातः । अस्य शिष्य उमानन्दाख्यः । उमानन्देन नित्योत्सव इति ग्रन्थ कृतः यश्च मद्रासनगरे (R. 2462 MGOML) लभ्यते । भासुरानन्देन रत्नावलीनामा ग्रन्थेऽपि कृत इति ज्ञायते ।। </p>
                <h2>3. मुकुन्दमुनिकृता - अष्टावक्रगीताव्याख्या । </h2>
                <p>ग्रन्थोऽयमपि मध्यप्रान्तीयवरार्ग्रन्थसूच्यां दृश्यते । अस्य कर्ता मुकुन्दमुनिः हरिनाथप्रशिष्यः रामनाथशिष्यः ब्रह्मामृतवर्षिणीकर्तू रामकिङ्करधर्मस्य गुरुः, महाराष्टदेशीयः, मुकुन्दानन्दमुक्तिनाथमुकुन्दराजापराभिधानस्सुज्ञानविंशतिकाराद्भिन्नः सप्तदशशतकीय (1550 - 1650 A.D.) इति निश्चीयते ।। </p>
                <h2>4. विश्वेश्वरपण्डितकृता - अष्टावक्रदीपिका ।</h2>
                <p>अवधूतानुभूतिदीपिका, अध्यात्मदीपिका, अष्टावक्रदीपिका इत्यपरनामायं ग्रन्थ आष्टेकरकम्पेनि पूनानगरे मुद्रितः । अस्य कर्ता विश्वेश्वरपण्डितः माध्वप्रज्ञशिष्य इति परं ज्ञायते ।। </p>
            </section>
            <section>
                <h1>ईश्वरगीता</h1>
                <p>कूर्मपुराणान्तर्गतेयं ईश्वरगीता । द्वादशभिरध्यायैः पूर्णेयं गीता ऋषिव्याससंवादमुखेन प्रवृत्ता बिब्लियोथिकासंस्कृतग्रन्थमालायां मुद्रिता । </p>
                <h2>1. यज्ञेश्वरसूरिकृता - ईश्वरगीताव्याख्या ।</h2>
                <p>ईश्वरगींताभाष्यमित्यपरनामायं ग्रन्थ अमुद्रितस्प्तरस्वतीमहालये (8997 DC. TSML) लभ्यते । अस्य कर्ता काश्यपगोत्रजः बहवृचशाखाध्यायी चर्कूरिवंशजः कोण्डुभट्ट - गङ्गाम्बिकयोः पुत्रः, तिरुमलैदीक्षितकनीयान् भ्राता यज्ञेश्वरकृष्णाश्रमयोः शिष्यः चोलदेशीयः, शाहेन्द्रग्रामवासी नृसिम्हाश्रमिण अर्वाचीनस्सप्तदशशतकीयः (1600 - 1700 A.D) शास्त्रदीपिकाव्याख्याप्रभामण्डलकर्ता यज्ञेश्वरदीक्षित इति पञ्चपादिकाविवरणोज्जीविनी (R. 592 MGOML) ईश्वरगीताव्याख्यानाच्च ज्ञायते ।। </p>
            </section>
            <section>
                <h1>उत्तरगीता</h1>
                <p>कृष्णार्जुनसंवादात्मकोऽयं ग्रन्थः प्रणवोपासनादीनद्वैतवेदान्तप्रकरणग्रन्थप्रतिपादितान् विषयान् त्रिभिरध्यायैः प्रतिपादयन् महाभारतान्तर्गतः वाणीविलासमुद्रणालये कृलकत्तायां बाम्बे नगर्याञ्च मुद्रितः । </p>
                <h2>1. काण्डद्वयातीतयोगिकृता -उत्तरगीताव्याख्या </h2>
                <p>अमुद्रितोऽयं ग्रन्थस्सरस्वतीमहालयपुस्तकालये (7568 DC TSML) लभ्यते । अस्य कर्ता हारीतगोत्रजः अतिवर्णाश्रमी सन्यासात् पूर्वं काण्डयार्यनीलाम्बयोः पुत्रः सामराजतीर्थशिष्यः काण्डद्वयातीतयोगीति ज्ञायते । यद्ययं सामराजः रतिकल्लोलिनीश्रीदामचरितशृङ्गारामृतलहर्यादिकर्ता नरहरिदीक्षितपुत्रः आनन्दरायकालिकः मधुरावासी सामराजस्स्यात्तर्हि काण्डद्वयातीतयोगिनः कालः 1775 सं. 1719 A. D.  इति निर्णेतुं शक्यते । </p>
                <h2>2. गौडपादाचार्यकृता उत्तरगीतादीपिका </h2>
                <p>मुद्रितोऽयं ग्रन्थः वाणीविलासमुद्रणालये । अस्य कर्ता गौडपादः किं माण्डूक्यकारिकाकर्ता ? उतान्य इत्यत्र न निश्चयः ।। </p>
                <h2>3. परमानन्दतीर्थकृता - उत्तरगीताटीका </h2>
                <p>ग्रन्थोऽयं महीशूरपुस्तकसूच्यां दृश्यते । परमानन्दतीर्थोऽयं विद्यातीर्थप्रशिष्यः भारतीतीर्थशिष्यश्चतुर्दशशतकीय (1300 - 1400 A.D.) इति ज्ञायते ।। </p>
                <h2>4. अज्ञातकर्तृकः - उत्तरगीतासारः - </h2>
                <p>गीतासार इत्यपरनामायं ग्रन्थः महीशूरसूच्यां (Vol. II Page 22) दृश्यते । आनन्दगिरिणा कृता च काचन व्याख्या मुद्रिता विद्यते ।। </p>
            </section>
