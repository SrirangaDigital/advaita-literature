                <h1>बोधानन्दगीता</h1>
                <p>ईशादिबृहदारण्यकान्तानां दशोपनिषदां भाष्यानुसारिणमर्थं सङ्गगृह्णन्तीयं गीता द्वादशमिः परिच्छेदैः पूर्णाऽमुद्रिता च तिरुवनन्तपुरं पुस्तकालये (316 DCTCL) लभ्यते । </p>
