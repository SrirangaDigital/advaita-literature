            <h4>एकमेवाद्वितीयं परमार्थसत्यम् ।</h4>
            <div class="description">

                <p>Advaita siddhanta bases its conclusion on Prasthanatraya, the three sources of authority. Prasthanatraya, (literally meaning three ways) is the collective name given to the Upanishads (Shruti prasthana), Brahma Sutra (Nyaya prasthana) and Srimad Bhagavat Gita (Smriti prasthana). Although Sri Shankaracharaya is regarded as the promoter of Advaita siddhanta as a distinct school of Indian philosophy, the origins of this school predate Sri Shankaracharaya. The existence of an Advaita tradition is acknowledged by Sri Shankaracharaya in his commentaries.</p>
                <p>A Vast amount of literature exists propounding this Advaita siddhanta, starting from the Prasthanatraiya Bhashya of Sri Shankaracharaya; though there are references to texts prior to Sri Shankaracharya, except for Sri Gaudapada’s Mandukya Karika, none are available. The texts are classified into different forms depending on their structure and purpose, which reflects the richness of Samskrita literature:</p>
                <ul>
                    <li>
                        <h5 style="color: #fd7f01;">Sutra (सूत्रं ):</h5>
                        <p>अल्पाक्षरं असंदिग्धं सारवत् विश्वतोमुखम् ।अस्तोभं अनवद्यं च सूत्रं सूत्र विदो विदुः॥</p>
                        <p>Example: Brahmasutras of Badarayana, Yoga sutras of Patanjali, Vyakarana sutras of Panini etc.</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Bhashya (भाष्यं):</h5>
                        <p>सूत्रार्थो वर्ण्यते यत्र, पदै: सुत्रानुसारिभिः।स्वपदानि च वर्ण्यन्ते, भाष्यं भाष्यविदो विदु: ॥ Example: Prasthanatraya Bhashya of Sri Shankaracharya, Patanjali’s Maha Bhashaya for Panini Sutras etc.</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Karika (कारिका):</h5>
                        <p>अल्पाक्षरत्वे सति बह्वर्थज्ञापकः श्लोकः ।</p>
                        <p>Example: Mandukya Karika of Sri Gaudapada.</p>
                    </li> 
                    <li>
                        <h5 style="color: #fd7f01;">Vartika (वार्तिका):</h5>
                        <p>उक्तानुक्तदुरुक्तानां चिन्ता यत्र प्रवर्तते । तं ग्रन्थं वार्तिकं प्राहुः वार्तिकज्ञा मनीषिणः ॥</p>
                        <p>Example: Sri Sureshwaracharya’s Taittiriya / Brihadaranyaka Bhashya vartika, Yoga vartika of Vijnana Bhikshu</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Vyakhya (वयाख्या):</h5>
                        <p>पदच्छेदः पदार्थोक्तिर्विग्रहो वाक्ययोजना। आक्षेपस्य समाधानं व्याख्यानं पञ्चलक्षणम् ॥</p>
                        <p>Example: Bhashyaratnaprabha Vyakhya of Sri Ramananda Yati</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Thika (टीका):</h5>
                        <p>मूलग्रन्थस्य अप्रतिपत्तिविप्रतिपत्त्यन्यथाप्रतिपत्तिनिवारणेन तत्कर्तुरभिप्रेतार्थस्य शब्दान्तरेण विवरणम् ।</p>
                        <p>Example: Anandagiri Tika for Prasthanatraya Bhashya, Tattvavaisharadi Thika for Vyasa Bhashya of Vachaspati Mishra</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Vritti (वृत्ति):</h5>
                        <p>सूत्रमात्रव्याख्या ।</p>
                        <p>Example: Brahmasutra Vritti of Sri Sadashiva Brahmendra</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Prakarana (प्रकरण):</h5>
                        <p>शास्त्रैकदेशसम्बद्दम् शास्त्रकार्यान्तरे स्थितम् ।आहुः प्रकरणम् नाम ग्रथमेदं विपश्चितः ॥</p>
                        <p>Example: Vivekachudamani, Aparokshanubhuti, Sri Dakshinamurthy stotra</p>
                    </li>
                    <li>
                        <h5 style="color: #fd7f01;">Tippani (टिप्पणी):</h5>
