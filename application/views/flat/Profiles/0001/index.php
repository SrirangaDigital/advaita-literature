            <p>शङ्कराचार्यः यस्यान्ये अभिधाने शङ्करभगवत्पादाचार्यः तथा च आदिशङ्कराचार्यः इति&nbsp;। आदिशङ्कराचार्यः एकः भारतीयः दार्शनिकः आसीत्, येन अद्वैतवेदान्तस्य सिद्धान्तः दृढतया प्रोद्बलितः&nbsp;। अद्वैतवेदान्तः वेदान्तस्य उपसंप्रदायः वर्तते&nbsp;। पूर्वमीमांसा इत्यपि एतस्य अपरं नाम&nbsp;। अद्वैतवेदान्तानुसारं जीवब्रह्मणोः अभेदः ब्रह्मणः निर्गुणत्वञ्च उच्यते&nbsp;। शङ्करः समग्रभारतवर्षस्य यात्रां कृत्वा स्वकीयं दर्शनं प्रवचनेभ्यः शास्त्रार्थेभ्यश्च प्रसारितवान्&nbsp;। सः दशनामी इति अद्वैतसंप्रदायस्य संघटनकर्ता मन्यते, तथा च षण्मत इति पूजनपरम्परायाः संस्थापकः मन्यते&nbsp;।</p>
            <p>संस्कृतेन रचिताः तस्य कृतयः, याः सर्वाः अपि अद्यापि प्राप्यमानाः, अद्वैतवेदान्तसिद्धान्तस्थापनविषयकाः सन्ति&nbsp;। सः उपनिषद्ब्रह्मसूत्रसम्मतस्य संन्यासस्य महत्तामपि प्रतिपादितवान्, यस्मिन् काले मीमांसा संप्रदायेन दृढं कर्मकाण्डं स्थापितमासीत् संन्यासश्च निन्दित: आसीत्&nbsp;। शङ्करः ब्रह्मविषये उपनिषदः प्रामाण्यं प्रोक्तवान्&nbsp;। तथा च ब्रह्मसूत्राणाम्, उपनिषदां, भगवद्गीतायाः च भाष्यं लिखितवान्&nbsp;। कर्मवादस्स्य प्रवर्तकेन मण्डनमिश्रेण सह ज्ञानवादं मण्डयित्वा जितवान्&nbsp;।</p>

