            <h4>D P Santhanam <small>(1926 - 2012)</small></h4>
            <div class="description">
                <p style="color: #fd7f01;">यस्मान्नोद्विजते लोको लोकान्नोद्विजते च यः ।<br />हर्षामर्षभयोद्वेगैर्मुक्तो यः स च मे प्रियः ॥ भगवद्गीता, १२-१५ ॥</p>
                <p><em>(He for whom no one is put into difficulty and who is not disturbed by anyone, who is equipoised in happiness and distress, fear and anxiety, is very dear to Me.)</em></p>
                <p style="color: #fd7f01;">ये तु धर्म्यामृतमिदं यथोक्तं पर्युपासते ।<br /> श्रद्दधाना मत्परमा भक्तास्तेऽतीव मे प्रियाः ॥ भगवद्गीता, १२-२० ॥</p>
                <p><em>(Those who follow this imperishable path of devotional service and who completely engage themselves with faith, making Me the supreme goal, are very, very dear to Me.)</em></p>
                <p>D P Santhanam was not a man of renown. He was, in fact, a self-effacing man like several millions of Indians who, with little learning, imbibe the way of Dharma by living immersed in the prescriptions of their Gurus, who most of the time are their parents and grand-parents. </p>
                <p>Santhanam was one such man. Failing his matriculation, he ran away to join the Royal Air Force as an airman. After the war, over the next six decades he held a series of low to moderately paying jobs. </p>
                <p>He was a God-fearing, hard working, frugal man who, though remaining single, was devoted to his extended family of sisters and their children. He shared his money, time, labours and affection freely. He was loyal to his employers and generous to the people who served his spartan home. Thus as per the words of Sri Krishna himself, Santhanam would be very dear to Him.</p>
                <p>When he retired at 85, a few months before he died, he was bemused to discover that so much had accumulated in his account. He read in that the message that a good part was meant to be given away for public causes. </p>
                <p>Sri D V Sridharan, his nephew, acting on behalf of D P Santhanam is pleased to contribute Rs. 5 Lakhs to fund the Advaitadhārā Project.</p>
